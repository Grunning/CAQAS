package com.CAQAS.model;

public class UserResultModel {
	private Integer userId;
	private String userName;
	private String userPassword;
	private Integer userRole;
	private String userMail;
	private String userDepId;
	private String userDepName;
	
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public Integer getUserRole() {
		return userRole;
	}
	public void setUserRole(Integer userRole) {
		this.userRole = userRole;
	}
	public String getUserMail() {
		return userMail;
	}
	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}
	public String getUserDepId() {
		return userDepId;
	}
	public void setUserDepId(String userDepId) {
		this.userDepId = userDepId;
	}
	public String getUserDepName() {
		return userDepName;
	}
	public void setUserDepName(String userDepName) {
		this.userDepName = userDepName;
	}
	
}
